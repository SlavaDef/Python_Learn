num1 = 6
print(num1, 'is of type', type(num1)) # 6 is of type <class 'int'>

num2 = 7.34
print(num2, 'is of type', type(num2)) # 7.34 is of type <class 'float'>

num3 = 3 + 5j
print(num3, 'is of type', type(num3)) # (3+5j) is of type <class 'complex'>

print(0b1101011)  # двійкова система числення, виведе 107

print(0o15)  # вісімкова система числення, виведе 13

print(0xFB + 0b10)  # шістнадцяткова система числення, виведе 253

num1 = int(4.3)
print(num1)  # виведе 4

num2 = int(-3.7)
print(num2)  # виведе -3

num3 = float(6)
print(num3)  # виведе 6.0

num4 = complex('4+6j')
print(num4)  # виведе (4 + 6j)

