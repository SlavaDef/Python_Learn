#
# Було: result = lambda x, y: x - (x * y / 100)
#
# Стало: calculate_discount(x, y)
#
# Це підвищує читабельність та полегшує розширення функціоналу.
#
# Обробка помилок за допомогою try-except:
#
# Тепер програма не зламається, якщо користувач введе, наприклад, "abc".
#
