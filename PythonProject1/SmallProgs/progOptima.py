def calculate_discount(x, y):
    return x - (x * y / 100)

print('Програма розраховує мінус процент від першого числа. Введіть 0 для виходу.')

while True:
    try:
        # Введення першого числа
        a = float(input("Введіть число: "))
        if a == 0:
            print("На все добре до нових зустрічей!")
            break

        # Введення відсотку
        b = float(input("Введіть процент: "))

        print(f"Результат: {calculate_discount(a, b)}\n")

    except ValueError:
        print("❌ Кривий ввід. Введіть коректне число!\n")


# Що змінилося та чому це краще:
